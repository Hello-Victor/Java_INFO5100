    public void populateCombobox() {
        jComboBox2.removeAllItems();
        assignees = new ArrayList<Employee>();
        for (Employee e : rescueOrganization.getEmployeeDirectory().getEmployeeList()) {
            if (e != rescueOrganization.getAdminEmployee() && e.isIsAvailable()) {
                jComboBox2.addItem(e.getId() + " - " + e.getName());
                assignees.add(e);
            }
        }
    }

    public void populateTable1() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        Object[] columns = {"Time", "Name", "Location", "Status", "Feedback"};
        model.setRowCount(0);
        model.setColumnIdentifiers(columns);
        for (int i = rescueOrganization.getWorkQueue().getWorkRequestList().size() - 1; i >= 0; i--) {
            WorkRequest request = rescueOrganization.getWorkQueue().getWorkRequestList().get(i);
            Object[] row = new Object[5];
            row[0] = request;
            row[1] = request.getSender().getEmployee().getName();
            row[2] = request.getLocation();
            row[3] = request.getStatusString();
            row[4] = request.getFeedbackString();
            model.addRow(row);
        }
    }

    public void populateTable2() {
        DefaultTableModel model = (DefaultTableModel) jTable3.getModel();
        Object[] columns = {"Time", "Name", "Location", "Status", "Feedback"};
        model.setRowCount(0);
        model.setColumnIdentifiers(columns);
        for (int i = rescueOrganization.getWorkQueue().getWorkRequestList().size() - 1; i >= 0; i--) {
            RescueRequest request1 = (RescueRequest) rescueOrganization.getWorkQueue().getWorkRequestList().get(i);
            if (request1.getHospitalRequest() != null) {
                HospitalRequest request = request1.getHospitalRequest();
                Object[] row = new Object[5];
                row[0] = request;
                row[1] = request.getSender().getEmployee().getName();
                row[2] = request.getLocation();
                row[3] = request.getStatusString();
                row[4] = request.getFeedbackString();
                model.addRow(row);
            }
        }

    }

